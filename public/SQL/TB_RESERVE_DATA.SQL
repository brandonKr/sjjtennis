CREATE TABLE ssjang.TB_RESERVE_DATA (
	SEQ BIGINT auto_increment not NULL COMMENT '순번',
	NAME varchar(100) NULL COMMENT '신청자이름',
	PHONE varchar(100) NULL COMMENT '전화번호',
	RESERVE_DAY varchar(100) NULL COMMENT '예약일',
	ST_HOUR varchar(2) NULL COMMENT '시작시간',
	ED_HOUR varchar(100) NULL COMMENT '종료시각',
	REG_DATE DATETIME NULL COMMENT '등록일자',
	UPD_DATE DATETIME NULL COMMENT '수정일자',
	DEL_YN varchar(2) NULL COMMENT '삭제여부',
	CONSTRAINT CUSTOMER_PK PRIMARY KEY (SEQ)
)
ENGINE=InnoDB
DEFAULT CHARSET=euckr
COLLATE=euckr_korean_ci;



